	<h1>About this</h1>

	<p>
		As the title suggests this is an experiment to see what analytics
		could be recorded with Turso and SvelteKit.
	</p>
	<p>
		Session data stored for no longer than 24 hours and deleted after
		the session ends.
	</p>
	<p>Location data is anonymised and stored with no related data.</p>

	<h2>Technologies used:</h2>
	<ul>
		<li>Svelte 5 runes mode</li>
		<li>SvelteKit</li>
		<li>Turso</li>
		<li>daisyUI</li>
		<li>Tailwind</li>
		<li>ipinfo.io</li>
	</ul>
</div>
